/**
 * ดึงข้อมูลไม้ผลทั้งหมด
 */
export function getAllFruitTrees(): FruitTreeInfo[] {
  return FRUIT_TREES;
}

/**
 * ดึงข้อมูลไม้ผลตาม ID
 */
export function getFruitTreeById(id: string): FruitTreeInfo | undefined {
  return FRUIT_TREES.find((tree) => tree.id === id);
}

/**
 * ดึงข้อมูลไม้ผลตามหมวดหมู่
 */
export function getFruitTreesByCategory(category: FruitCategory): FruitTreeInfo[] {
  return FRUIT_TREES.filter((tree) => tree.category === category);
}

/**
 * ดึงรายชื่อหมวดหมู่ทั้งหมด
 */
export function getAllCategories(): FruitCategory[] {
  return ["ไม้ผลเมืองร้อน", "ไม้ผลเมืองหนาว", "ไม้ผลอื่นๆ", "พืชเศรษฐกิจ", "พืชผัก"];
}

/**
 * ดึงข้อมูลไม้ผลสำหรับแสดงผลแบบสรุป
 */
export function getFruitTreeSummaries(currentMonth: number = new Date().getMonth()): FruitTreeSummary[] {
  return FRUIT_TREES.map((tree) => {
    const monthData = tree.monthlyActivities[currentMonth];
    const currentActivity = monthData?.activities[0]?.activity || "-";
    const isHarvest = currentActivity.includes("เก็บเกี่ยว");

    return {
      id: tree.id,
      name: tree.name,
      category: tree.category,
      image: tree.image,
      currentMonthActivity: currentActivity,
      isHarvestMonth: isHarvest,
    };
  });
}

/**
 * ดึงไม้ผลที่กำลังเข้าสู่ช่วงเก็บเกี่ยวในเดือนนี้
 */
export function getHarvestingFruitTrees(month: number = new Date().getMonth()): FruitTreeInfo[] {
  return FRUIT_TREES.filter((tree) => {
    const monthData = tree.monthlyActivities[month];
    return monthData?.activities.some((act) => act.activity.includes("เก็บเกี่ยว"));
  });
}

/**
 * ดึงไม้ผลตามแท็ก
 */
export function getFruitTreesByTag(tag: string): FruitTreeInfo[] {
  return FRUIT_TREES.filter((tree) => tree.tags?.includes(tag));
}

/**
 * ค้นหาไม้ผล
 */
export function searchFruitTrees(query: string): FruitTreeInfo[] {
  const lowerQuery = query.toLowerCase();
  return FRUIT_TREES.filter(
    (tree) =>
      tree.name.toLowerCase().includes(lowerQuery) ||
      tree.tags?.some((tag) => tag.toLowerCase().includes(lowerQuery)) ||
      tree.description.toLowerCase().includes(lowerQuery)
  );
}

/**
 * ดึงกิจกรรมของไม้ผลในเดือนที่ระบุ
 */
export function getActivitiesForMonth(treeId: string, month: number): FruitTreeActivity[] {
  const tree = getFruitTreeById(treeId);
  if (!tree) return [];
  
  return tree.monthlyActivities[month]?.activities || [];
}

/**
 * ดึงตารางปุ๋ยของไม้ผลในเดือนที่ระบุ
 */
export function getFertilizersForMonth(treeId: string, month: number): FertilizerSchedule[] {
  const tree = getFruitTreeById(treeId);
  if (!tree) return [];
  
  return tree.fertilizers.filter((fert) => fert.month === month);
}

/**
 * ตรวจสอบว่าเดือนนี้เป็นเดือนเก็บเกี่ยวหรือไม่
 */
export function isHarvestMonth(treeId: string, month: number): boolean {
  const activities = getActivitiesForMonth(treeId, month);
  return activities.some((act) => act.activity.includes("เก็บเกี่ยว"));
}

/**
 * ดึงสี CSS จาก ActivityColor
 */
export function getActivityColorClasses(color: ActivityColor) {
  return ACTIVITY_COLORS[color];
}

/**
 * ดึงชื่อเดือนภาษาไทย
 */
export function getThaiMonthName(month: number): string {
  return THAI_MONTHS[month]?.full || "";
}

