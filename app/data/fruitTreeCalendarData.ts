/**
 * แปลงข้อความกิจกรรมเป็น ActivityColor
 */
function getActivityColor(activity: string): ActivityColor {
  const activityLower = activity.toLowerCase();
  
  if (activityLower.includes("ปลูก")) return "green";
  if (activityLower.includes("ออกดอก")) return "pink";
  if (activityLower.includes("ออกผล") || activityLower.includes("ติดผล")) return "orange";
  if (activityLower.includes("เก็บเกี่ยว")) return "blue";
  if (activityLower.includes("ตัดแต่ง")) return "purple";
  if (activityLower.includes("ใส่ปุ๋ย") || activityLower.includes("ให้น้ำ")) return "yellow";
  if (activityLower.includes("แตกใบ") || activityLower.includes("แตกกิ่ง")) return "red";
  if (activityLower.includes("ดูแล") || activityLower.includes("บำรุง")) return "cyan";
  if (activityLower.includes("เตรียม")) return "gray";
  if (activityLower.includes("พักต้น")) return "gray";
  
  return "gray";
}

/**
 * แปลงข้อความกิจกรรมเป็น ActivityIcon
 */
function getActivityIcon(activity: string): ActivityIcon {
  const activityLower = activity.toLowerCase();
  
  if (activityLower.includes("ปลูก")) return "🌱";
  if (activityLower.includes("ออกดอก")) return "🌸";
  if (activityLower.includes("ออกผล") || activityLower.includes("ติดผล")) return "🍊";
  if (activityLower.includes("เก็บเกี่ยว")) return "🧺";
  if (activityLower.includes("ตัดแต่ง")) return "✂️";
  if (activityLower.includes("ใส่ปุ๋ย")) return "💧";
  if (activityLower.includes("ให้น้ำ")) return "💧";
  if (activityLower.includes("แตกใบ") || activityLower.includes("แตกกิ่ง")) return "🌳";
  if (activityLower.includes("ดูแล") || activityLower.includes("บำรุง")) return "🌿";
  if (activityLower.includes("เตรียม")) return "🔧";
  if (activityLower.includes("พักต้น")) return "⏸️";
  
  return "❓";
}

/**
 * แยกกิจกรรมหลายรายการจากข้อความ (เช่น "ออกดอก-ผลแก่-เก็บเกี่ยว")
 */
function parseActivities(activityText: string): FruitTreeActivity[] {
  if (!activityText || activityText.trim() === "") {
    return [{ activity: "-", color: "gray", icon: "⏸️" }];
  }

  // แยกด้วยตัวแยกต่างๆ
  const separators = /[-\/–—|,]+/;
  const parts = activityText.split(separators).map(s => s.trim()).filter(s => s.length > 0);

  if (parts.length === 0) {
    return [{ activity: activityText, color: getActivityColor(activityText), icon: getActivityIcon(activityText) }];
  }

  return parts.map(part => ({
    activity: part,
    color: getActivityColor(part),
    icon: getActivityIcon(part),
  }));
}

/**
 * สร้าง MonthlyActivity สำหรับทั้งปี
 */
function createMonthlyActivities(
  monthData: Record<string, string>,
  monthKeys: string[]
): MonthlyActivity[] {
  return monthKeys.map((key, index) => {
    const activityText = monthData[key] || "";
    const activities = parseActivities(activityText);
    
    return {
      month: index,
      monthName: THAI_MONTHS[index].full,
      monthShort: THAI_MONTHS[index].short,
      activities,
      themeColor: MONTH_THEME_COLORS[index],
    };
  });
}

/**
 * สร้าง slug จากชื่อไม้ผล
 */
function createSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/\s+/g, "-")
    .replace(/[()]/g, "")
    .replace(/[^a-z0-9-]/g, "")
    .replace(/-+/g, "-");
}

/**
 * แปลงชื่อหมวดหมู่จากชื่อไม้ผล
 */
function getCategoryFromName(name: string): FruitCategory {
  const nameLower = name.toLowerCase();
  
  if (nameLower.includes("กาแฟ") || nameLower.includes("มะพร้าว") || nameLower.includes("ชา")) {
    return "พืชเศรษฐกิจ";
  }
  if (nameLower.includes("สตรอเบอรี่")) {
    return "ไม้ผลเมืองหนาว";
  }
  if (nameLower.includes("มะละกอ") || nameLower.includes("ผัก")) {
    return "พืชผัก";
  }
  
  return "ไม้ผลเมืองร้อน";
}

