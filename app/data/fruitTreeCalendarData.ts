/**
 * แปลงข้อมูล FruitTree เป็นรูปแบบที่ใช้กับระบบเดิม
 * ใช้สำหรับการผสมผสานกับ CROP_OPTIONS และ CROP_FERTILIZER_DATA
 */
export function convertToLegacyFormat(tree: FruitTreeInfo): {
  cropName: string;
  monthlyFertilizers: { fertText: string; fertImg: string }[];
} {
  return {
    cropName: tree.name,
    monthlyFertilizers: tree.fertilizers.map((fert) => ({
      fertText: `${fert.formula} (${fert.purpose})`,
      fertImg: `/image/Fertilizer/${fert.formula}.png`,
    })),
  };
}

